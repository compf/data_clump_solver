package org.argouml.activity2.diagram;

public class NodeBounds {
    public final int x;
    public final int y;
    public final int w;
    public final int h;

    public NodeBounds(int x, int y, int w, int h) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }
}